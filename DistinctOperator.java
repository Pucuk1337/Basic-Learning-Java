public class DistinctOperator {
  public static void main(String[] args) {
    
    int a = 100;
    int b = 100;

    System.out.println("Perbandingan Lebih dari " + (a > b));
    System.out.println("Perbandingan Kurang dari " + (a < b));
    System.out.println("Perbandingan Lebih dari sama dengan " + (a >= b));
    System.out.println("Perbandingan Kurang dari sama dengan " + (a <= b));
    System.out.println("Perbandingan sama dengan " + (a == b));
    System.out.println("Perbandingan Tidak sama dengan " + (a != b));
  }
  
}
