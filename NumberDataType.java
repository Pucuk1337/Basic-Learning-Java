public class NumberDataType {
  public static void main(String[] args) {
    // Integer Number - Floating Point Number
    byte iniByte = 100;
    System.out.println(iniByte);
    short iniShort = 1000;
    System.out.println(iniShort);
    int iniInt = 10000000;
    System.out.println(iniInt);
    long iniLong = 100000000;
    System.out.println(iniLong);
    long iniLong2 = 100000000L;
    System.out.println(iniLong2);
    
    // Decimal Number - Floating Point Number
    float iniFloat = 10.10F;
    System.out.println(iniFloat);
    double iniDouble = 10.10;
    System.out.println(iniDouble);

    // Literals Point Number
    int decimalInt = 34;
    int hexaDecimal = 0xFFFFFF; // Using 0x
    int binaryDecimal = 0b101010; // Using 0b

    // Underscore Point Number
    long balance = 1_000_000_000l;
    long sum = 1_000_000;


  }
  
}
